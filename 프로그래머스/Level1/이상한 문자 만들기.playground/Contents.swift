func solution(_ s: String) -> String {
    return s.components(separatedBy: " ").map { string in
        string.enumerated().map { index, char in
            index % 2 == 0 ? char.uppercased() : char.lowercased()
        }
    }.map { $0.joined() }.joined(separator: " ")
}

